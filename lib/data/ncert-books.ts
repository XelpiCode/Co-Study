// Helper functions
export const getBooksByClass = (classNum: string): NCERTBook[] => {
  return NCERT_BOOKS.filter((book) => book.class === classNum);
};

export const getBooksBySubject = (classNum: string, subject: "Math" | "Science" | "Social Studies"): NCERTBook | undefined => {
  return NCERT_BOOKS.find((book) => book.class === classNum && book.subject === subject);
};

export const getAvailableClasses = (): string[] => {
  return Array.from(new Set(NCERT_BOOKS.map((book) => book.class))).sort();
};

export const getAvailableSubjects = (classNum: string): ("Math" | "Science" | "Social Studies")[] => {
  const books = getBooksByClass(classNum);
  return Array.from(new Set(books.map((book) => book.subject))) as ("Math" | "Science" | "Social Studies")[];
};

